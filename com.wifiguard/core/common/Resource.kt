/**
 * Универсальный wrapper-класс для инкапсуляции состояний операций.
 * Используется для представления результата операций (успех/ошибка/загрузка)
 * в архитектуре MVVM, что обеспечивает единообразную обработку состояний во всем приложении.
 * 
 * @param T Тип данных, которые возвращает операция при успешном выполнении
 */
sealed class Resource<T> {
    
    /**
     * Состояние успешного выполнения операции.
     * Содержит данные, полученные в результате операции.
     * 
     * @param data Данные, полученные при успешном выполнении операции
     */
    data class Success<T>(val data: T) : Resource<T>()
    
    /**
     * Состояние ошибки при выполнении операции.
     * Содержит исключение, которое произошло в процессе выполнения.
     * 
     * @param throwable Исключение, описывающее причину ошибки
     */
    data class Error<T>(val throwable: Throwable) : Resource<T>()
    
    /**
     * Состояние загрузки/выполнения операции.
     * Используется для отображения индикаторов загрузки в UI.
     * Не содержит данных, так как операция еще не завершена.
     */
    class Loading<T> : Resource<T>()
}