/**
 * Базовый интерфейс для маппинга (преобразования) объектов различных типов.
 * Используется для преобразования между слоями архитектуры:
 * - DTO (Data Transfer Object) ↔ Domain модель
 * - Entity (Room база данных) ↔ Domain модель
 * - Domain модель ↔ UI модель
 * 
 * Этот подход обеспечивает чистоту архитектуры и независимость слоев.
 *
 * @param From Исходный тип объекта, который нужно преобразовать
 * @param To Целевой тип объекта, к которому нужно привести
 */
interface Mapper<in From, out To> {
    
    /**
     * Метод для преобразования объекта исходного типа в объект целевого типа.
     * 
     * @param from Объект, который нужно преобразовать
     * @return Преобразованный объект целевого типа
     */
    fun map(from: From): To
}